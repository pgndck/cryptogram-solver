# Cryptogram Solver

A simple web-based tool for solving cryptograms.

## Features
- Input encoded text and solve cryptograms with ease.
- Frequency analysis of letters.
- Automatic saving and loading of the last puzzle using cookies.
- Responsive design using Bootstrap.

## How to Use
1. Open the website.
2. Paste the encoded text in the input field.
3. Press "Start Solving" to begin working on the puzzle.
4. Use the sidebar to view letter frequencies.

## Hosting
Host this project on GitHub Pages or any static web server.

